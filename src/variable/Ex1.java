package variable;

public class Ex1 {
    public static void main(String[] args) {

        // 참/거짓 - boolean
        System.out.println(true);
        System.out.println(false);

        // 숫자
        System.out.println(123); // int
        System.out.println(3.14); // double

        // 문자 - char
        System.out.println('A');

        // 문자열 - string
        System.out.println("안녕하세요");

    }
}
